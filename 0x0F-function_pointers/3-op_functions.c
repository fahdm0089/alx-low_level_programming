#include "3-calc.h"

/**
 * op_add - adds two integrs
 * @a: the first int
 * @b: the second int
 *
 * Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integrs
 * @a: the first int
 * @b: the second int
 *
 * Return: the diffrence
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul -  multiplies two integrs
 * @a: the first int
 * @b: the secont int
 *
 * Return: the product
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integrs
 * @a: the first int
 * @b: the second int
 *
 * Return: the quotient
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two integrs
 * @a: the first int
 * @b: the second int
 *
 * Return: the reimender
*/
int op_mod(int a, int b)
{
	return (a % b);
}

